// Функция для установки языка на странице
function setLanguageOnPage(langData, lang) {
    // Получаем все элементы с атрибутом data-lang-key
    let elements = document.querySelectorAll('[data-lang-key]');
    
    // Проходим по каждому элементу и устанавливаем текст в соответствии с выбранным языком
    elements.forEach(function(element) {
        let key = element.getAttribute('data-lang-key');
        element.textContent = langData[lang][key];
    });
}

function changeLanguage() {
    let select = document.getElementById("langSelect");
    let lang = select.options[select.selectedIndex].value;

    // Сохраняем выбранный язык в localStorage
    localStorage.setItem("selectedLanguage", lang);

    // Получаем языковые данные
    let langData = loadLanguageData();

    // Устанавливаем язык на странице
    setLanguageOnPage(langData, lang);

    // Меняем стилизацию объектов в зависимости от выбранного языка,
    // если элементы с соответствующими идентификаторами существуют на странице
    if (lang === "ru") {
        let heroTitle = document.getElementById("heroTitle");
        let heroBackgroundBlock = document.getElementById("heroBackgroundBlock");
        let projectsTitle = document.getElementById("projectsTitle");
        let contactsTitle = document.getElementById("contactsTitle");
        
        if (heroTitle) heroTitle.classList.add("russian");
        if (heroBackgroundBlock) heroBackgroundBlock.classList.add("russian");
        if (projectsTitle) projectsTitle.classList.add("russian");
        if (contactsTitle) contactsTitle.classList.add("russian");
    } else {
        let heroTitle = document.getElementById("heroTitle");
        let heroBackgroundBlock = document.getElementById("heroBackgroundBlock");
        let projectsTitle = document.getElementById("projectsTitle");
        let contactsTitle = document.getElementById("contactsTitle");
        
        if (heroTitle) heroTitle.classList.remove("russian");
        if (heroBackgroundBlock) heroBackgroundBlock.classList.remove("russian");
        if (projectsTitle) projectsTitle.classList.remove("russian");
        if (contactsTitle) contactsTitle.classList.remove("russian");
    }
}

document.addEventListener("DOMContentLoaded", function() {
    let lang = localStorage.getItem("selectedLanguage") || "en"; // Если язык не установлен в localStorage, используем en по умолчанию
    let select = document.getElementById("langSelect");
    select.value = lang; // Устанавливаем сохраненное значение языка в селекте

    // Вызываем функцию изменения языка, чтобы применить класс russian сразу после загрузки страницы
    changeLanguage();
});

document.getElementById("langSelect").addEventListener("change", changeLanguage);



